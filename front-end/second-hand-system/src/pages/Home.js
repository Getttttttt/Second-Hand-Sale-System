function mapString(inputString) {
  if (inputString === "Customer") {
    return "/customer";
  } else if (inputString === "Book") {
    return "/home";
  } else if (inputString === "Merchant" ){
    return "/merchant";
  } else if (inputString === 'Customer Sign In'){
    return "/"
  }
}


